import sys
ip = sys.stdin.readline
T = int(ip())
Q = [int(ip()) for i in range(T)]
N = max(Q)
dp = [[0,0,0,0] for _ in range(N+1)]
dp[1] = [0,1,0,0]
dp[2] = [0,1,1,0]
dp[3] = [0,1,1,1]

